package jeu;

/**
 * Cette énumération représente les différents types de paramètres
 * utilisés dans le jeu.
 * 
 * Les valeurs possibles sont :
 * - INPUT_OUTPUT_TYPE : le type de paramètre utilisé pour l'entrée/sortie.
 * - TYPE_J1 : le type de paramètre pour le joueur 1.
 * - TYPE_J2 : le type de paramètre pour le joueur 2.
 * - LISTE_BATEAUX : le type de paramètre pour la liste de bateaux.
 */
public enum TypeParam {
  INPUT_OUTPUT_TYPE,
  TYPE_J1,
  TYPE_J2,
  LISTE_BATEAUX;
}
