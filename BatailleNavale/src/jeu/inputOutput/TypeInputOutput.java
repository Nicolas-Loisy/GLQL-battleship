package jeu.inputOutput;

/**
 * Cette énumération représente les différents types d'InputOutput possibles.
 */
public enum TypeInputOutput {
  /**
   * Type console, pour l'entrée et la sortie via la console.
   */
  CONSOLE,
  
  /**
   * Type IU, pour l'entrée et la sortie via une interface utilisateur.
   */
  IU;
}
